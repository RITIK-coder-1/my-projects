const os = require("os")

console.log(os.arch()); // x64 // type of architecture
console.log(os.tmpdir()); // /tmp // temporary directory
console.log(os.freemem()); // 1952382976 // free memory available in bytes
console.log(os.hostname()); // penguin // name of the host
console.log(os.totalmem()); // 2898321408 // total memory in bytes
console.log(os.type()); // Linux // type of OS
console.log(os.platform()); // linux







