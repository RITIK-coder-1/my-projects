// Write a JavaScript program to check if a number is positive, negative, or zero -------->

let num = 0

if (num === 0) {
    console.log("It's a Zero!");
} else if (num < 0) {
    console.log(`The number ${num} is a negative integer!`);
} else {
    console.log(`The number ${num} is a positive integer!`);
}